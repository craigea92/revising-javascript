/*
PLUS PLUS

To increment a variable we can use the += operator. 
JS also has a ++ operator for when you only want to increment by 1.

let starRating = 4

starRating += 1
// starRating is now 5

starRating++
// starRating is now 6

starRating += 4
// starRating is now 10
*/

